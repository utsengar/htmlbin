// Minimum acceptable TOKEN_PEPPER length. The dev pepper generated by
// scripts/setup.mjs is 64 hex chars (32 bytes); 32 chars is a generous
// floor that still rejects empty / placeholder values.
const MIN_PEPPER_LENGTH = 32;

// ----- Global security headers --------------------------------------------
//
// Applied to every response. The split:
//   - Universal:  HSTS, X-Content-Type-Options, Referrer-Policy, Permissions-Policy.
//   - text/html:  X-Frame-Options + Content-Security-Policy (unless the handler
//                 already set its own CSP — /p/:slug/raw does for the iframed
//                 user-uploaded HTML; we don't overwrite it).
//   - application/json + linkset+json: default-src 'none' (defense in depth).
//
// CORS is intentionally NOT set anywhere: htmlbin is an agent-side API.
// Disallowing browser cross-origin XHR is a feature — it prevents a random
// third-party site from making authenticated calls with the user's token.
// If you find yourself wanting to add CORS, talk to the user first.
app.use("*", async (c, next) => {
  await next();
  const ct = c.res.headers.get("content-type") ?? "";

  c.res.headers.set(
    "Strict-Transport-Security",
    "max-age=31536000; includeSubDomains; preload"
  );
  c.res.headers.set("X-Content-Type-Options", "nosniff");
  c.res.headers.set("Referrer-Policy", "strict-origin-when-cross-origin");
  c.res.headers.set(
    "Permissions-Policy",
    "geolocation=(), camera=(), microphone=(), payment=(), usb=()"
  );

  if (ct.startsWith("text/html")) {
    // Don't clobber a handler-set CSP (e.g. /p/:slug/raw uses a different one
    // to allow the viewer to iframe it).
    if (!c.res.headers.has("content-security-policy")) {
      c.res.headers.set("X-Frame-Options", "SAMEORIGIN");
      const sentryOn = !!c.env.SENTRY_DSN;
      const scriptSrc = sentryOn
        ? "script-src 'self' 'unsafe-inline' https://js.sentry-cdn.com"
        : "script-src 'self' 'unsafe-inline'";
      const connectSrc = sentryOn
        ? "connect-src 'self' https://*.ingest.sentry.io https://*.ingest.us.sentry.io"
        : "connect-src 'self'";
      c.res.headers.set(
        "Content-Security-Policy",
        [
          "default-src 'self'",
          "img-src 'self' data: https:",
          // No external font origins now that we self-host Geist/Geist Mono.
          "style-src 'self' 'unsafe-inline'",
          "font-src 'self'",
          scriptSrc,
          connectSrc,
          "frame-src 'self'",
          "frame-ancestors 'none'",
          "base-uri 'none'",
        ].join("; ")
      );
    }
  } else if (
    ct.startsWith("application/json") ||
    ct.startsWith("application/linkset+json")
  ) {
    if (!c.res.headers.has("content-security-policy")) {
      c.res.headers.set("Content-Security-Policy", "default-src 'none'");
    }
  }
});

// ----- Required-config check ----------------------------------------------
//
// TOKEN_PEPPER is required for token hashing AND for HMAC-signed unlock
// cookies on passcode-protected drops. An empty/missing pepper causes
// `crypto.subtle.importKey` to throw `DataError: Imported HMAC key length
// (0)…` deep in the request — surfacing as an opaque 500. Catch it at the
// edge with a clear, machine-readable error so misconfigured deploys are
// loud, not silently broken.
app.use("*", async (c, next) => {
  const pepper = c.env.TOKEN_PEPPER;
  if (typeof pepper !== "string" || pepper.length < MIN_PEPPER_LENGTH) {
    return apiError(
      c,
      "server_misconfigured",
      "TOKEN_PEPPER secret is missing or too short. Set it with `wrangler secret put TOKEN_PEPPER`.",
      503
    );
  }
  await next();
});

// ----- Public landing -----------------------------------------------------
app.on(["GET", "HEAD"], "/", async (c) => {
  // Content-negotiation: agents asking for Markdown get the landing as
  // Markdown via Workers AI's toMarkdown. Same idea as Cloudflare's
  // "Markdown for Agents" pattern. Humans still get the regular HTML.
  const accept = c.req.header("Accept") ?? "";
  if (acceptsMarkdown(accept) || c.req.query("format") === "md") {
    return landingAsMarkdown(c);
  }
  c.header("Link", linkHeader(c.env.PUBLIC_URL));
  return c.html(landingPage(c.env));
});

