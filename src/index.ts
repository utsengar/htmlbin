function pngResponse(body: ArrayBuffer, fromCache = false): Response {
  return new Response(body, {
    headers: {
      "Content-Type": "image/png",
      "Cache-Control": "public, max-age=300, s-maxage=86400",
      "X-Og-Cache": fromCache ? "hit" : "miss",
    },
  });
}

